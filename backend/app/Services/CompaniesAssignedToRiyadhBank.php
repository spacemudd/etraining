    {
        if (in_array($company_id, $this->list) || (Company::find($company_id)->created_at >= Carbon::parse('2023-02-09'))) {
            config(['tap-payment.auth.api_key' => env('TAP_PAYMENT_API_KEY_SECONDARY')]);
        }
    }

